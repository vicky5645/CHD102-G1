<script>
import { Modal } from "bootstrap";
import axios from "axios";
import { BASE_URL } from "@/assets/js/common.js";

export default {
  data() {
    return {
      dataFromMySQL: [],

      // search
      searchText: "",
      // model
      currentItem: {},
      backupItem: {},
      showModal: false,
      selectedFile: null,
      // new model
      newAnnouncement: {
        spot_no: "", // 確保 id 屬性存在
        spot_name: "",
        spot_info: "",
        spot_status: 0,
        spot_file: null,
      },
    };
  },

  computed: {
    // search
    filteredItems() {
      if (this.searchText === "") {
        return this.dataFromMySQL;
      }

      return this.dataFromMySQL.filter((item) =>
        Object.values(item).some((val) => String(val).includes(this.searchText))
      );
    },
  },

  methods: {
    //顯示上下架
    spotStatus(spot_status) {
      return spot_status == 1 ? "上架" : "下架";
    },
    //讓使用者只能輸入介於min到max
    checkInput(event) {
      let value = parseInt(event.target.value);
      let min = parseInt(event.target.min);
      let max = parseInt(event.target.max);
      if (value < min) {
        event.target.value = min;
      } else if (value > max) {
        event.target.value = max;
      }
    },

    cancelChanges() {
      this.currentItem = { ...this.backupItem };
      this.showModal = false;
    },
    openModal(item) {
      this.currentItem = item;
      this.showModal = true;

      this.$nextTick(() => {
        const modalElement = document.getElementById("itemModal");
        const modalInstance = new Modal(modalElement);
        modalInstance.show();

        modalElement.addEventListener("hide.bs.modal", () => {
          this.showModal = false;
        });
      });
    },
    handleFileUpload(event) {
      const files = event.target.files;
      if (files.length === 0) {
        return;
      }

      const file = files[0];
      const maxSize = 2 * 1024 * 1024; // 2MB
      if (file.size > maxSize) {
        alert("檔案大小超過 2MB");
        event.target.value = ""; // 清空
        return;
      }
      const reader = new FileReader();

      reader.onload = (e) => {
        this.newAnnouncement.spot_file = e.target.result;
        this.newAnnouncement.spot_file_name = file.name;
      };
      reader.readAsDataURL(file);
    },
    //edit model
    saveChanges() {
      // 在這裡更新資料
      // 如有需要，你也可以將 currentItem 傳到後端
      // this.currentItem = { ...this.backupItem };
      this.showModal = false;
      const index = this.dataFromMySQL.findIndex(
        (item) => item.spot_no === this.currentItem.spot_no
      );
      if (index !== -1) {
        // 這裡刪除的是vue this.data
        this.dataFromMySQL.splice(index, 1);
        this.showModal = false;
      }
      //傳送資料庫的資料
      // 修改圖檔時，接收變數
      let imgFile = this.newAnnouncement.spot_file;
      const newModel = this.newAnnouncement;

      const data = new FormData(); // POST 表單資料
      data.append("spot_no", this.currentItem.spot_no);
      data.append("spot_name", this.currentItem.spot_name);
      data.append("spot_info", this.currentItem.spot_info);
      data.append("spot_status", this.currentItem.spot_status);

      data.append("news_filename", newModel.pattern_file_name);

      if (imgFile) {
        data.append("type", "editimg");
        data.append("spot_file", this.currentItem.spot_file);
        data.append("news_filename", this.newAnnouncement.spot_file_name); // imgFile 是文件对象
        data.append("news_img", imgFile);
        console.log("news_img", imgFile);
      }

      // 使用 Axios 發送 POST 請求
      axios
        .post(`${BASE_URL}editSpot.php`, data)
        .then((response) => {
          // 請求成功後的處理
          console.log(response.data);
          // 重新取得資料
          alert("已修改圖案成功！");
          this.getdataFromMySQL();
        })
        .catch((error) => {
          // 請求失敗後的處理
          console.error(error);
          alert("修改失敗！");
        });
    },

    // new model
    submitAnnouncement() {
      const newModel = this.newAnnouncement;
      const imgFile = this.newAnnouncement.spot_file;
      if (!this.newAnnouncement.spot_name || !this.newAnnouncement.spot_info) {
        alert("所有欄位都必須填寫！");
        return;
      }

      // 準備要傳送的資料
      const data = new FormData(); // POST 表單資料
      data.append("spot_no", newModel.spot_no);
      data.append("spot_name", newModel.spot_name);
      data.append("spot_info", newModel.spot_info);
      data.append("spot_status", newModel.spot_status);
      data.append("news_filename", this.newAnnouncement.spot_file_name);

      if (newModel.spot_file) {
        data.append("type", "addImg");
        data.append("news_img", imgFile);
      }

      // 使用 Axios 發送 POST 請求
      axios
        .post(`${BASE_URL}postSpot.php`, data)
        .then((response) => {
          // 請求成功後的處理
          console.log(response.data);
          // 重新取得資料
          alert("新增成功！");
          this.getdataFromMySQL();
        })
        .catch((error) => {
          // 請求失敗後的處理
          console.error(error);
          alert("新增失敗！");
        });
      this.getdataFromMySQL();

      this.clearAnnouncement();

      const modalEl = document.getElementById("itemNewModal");
      const modalInstance = Modal.getInstance(modalEl);
      modalInstance.hide();
    },

    clearAnnouncement() {
      this.newAnnouncement = {
        spot_no: "", // 確保 id 屬性存在
        spot_name: "",
        spot_info: "",
        spot_status: 0,
        spot_file: null,
      };
    },

    // delete announcement
    deleteAnnouncement() {
      const index = this.dataFromMySQL.findIndex(
        (item) => item.spot_no === this.currentItem.spot_no
      );
      if (index !== -1) {
        this.dataFromMySQL.splice(index, 1);
        this.showModal = false;
      }

      //傳送資料庫要刪除的項目
      const data = new URLSearchParams();
      data.append("spot_no", this.currentItem.spot_no);
      data.append("spot_file", this.currentItem.spot_file);
      // 使用 Axios 發送 POST 請求
      axios
        .post(`${BASE_URL}deleteSpot.php`, data)
        .then((response) => {
          // 請求成功後的處理
          console.log(response.data);
          location.reload(); //刷新頁面
          alert("刪除圖案成功！");
        })
        .catch((error) => {
          // 請求失敗後的處理
          console.error(error);
          alert("刪除失敗！");
        });
    },

    //取資料
    async getdataFromMySQL() {
      // 設定要執行的操作，這裡是取得資料
      axios
        .get(`${BASE_URL}/getSpot.php`)
        .then((response) => {
          this.dataFromMySQL = response.data;
        })
        .catch((error) => {
          console.error("There was an error fetching the data:", error);
        });
    },
  },
  created() {
    this.getdataFromMySQL();
  },
};
</script>

